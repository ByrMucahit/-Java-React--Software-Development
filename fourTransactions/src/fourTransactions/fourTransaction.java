package fourTransactions;

public class fourTransaction {
	
	public int sum(int num1, int num2) {
		return num1 + num2;
	}
	public int multipy(int num1, int num2) {
		return num1 * num2;
	}
	public float division(int num1, int num2) {
		return num1/num2;
	}
	public int minus(int num1, int num2) {
		return num1 - num2;
	}
}
